package cn.syrjia.hospital.entity;

import java.io.Serializable;

import cn.syrjia.common.annotation.Id;
import cn.syrjia.common.annotation.Table;

@Table(name="t_doctor_conditioning")
public class DoctorConditioning implements Serializable{

	private static final long serialVersionUID = 1L;
	
	/**
	 * id
	 */
	@Id(increment=false)
	private String id;
	
	/**
	 * 名称
	 */
	private String name;
	
	/**
	 * 状态
	 */
	private Integer state;
	
	/**
	 * 类型
	 */
	private Integer type;
	
	/**
	 * 创建用户id
	 */
	private String createUserId;
	
	/**
	 * 是医生
	 */
	private Integer isDoctor;
	
	/**
	 * 出处
	 */
	private String provenance;
	
	/**
	 * 作者
	 */
	private String author;
	
	/**
	 * 主治
	 */
	private String attending;
	
	/**
	 * 创建时间
	 */
	private Integer createTime;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getState() {
		return state;
	}
	public void setState(Integer state) {
		this.state = state;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public String getCreateUserId() {
		return createUserId;
	}
	public void setCreateUserId(String createUserId) {
		this.createUserId = createUserId;
	}
	public Integer getIsDoctor() {
		return isDoctor;
	}
	public void setIsDoctor(Integer isDoctor) {
		this.isDoctor = isDoctor;
	}
	public String getProvenance() {
		return provenance;
	}
	public void setProvenance(String provenance) {
		this.provenance = provenance;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getAttending() {
		return attending;
	}
	public void setAttending(String attending) {
		this.attending = attending;
	}
	public Integer getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Integer createTime) {
		this.createTime = createTime;
	}

}
