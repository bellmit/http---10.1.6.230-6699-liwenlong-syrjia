package cn.syrjia.hospital.entity;

import java.io.Serializable;

import cn.syrjia.common.annotation.Id;
import cn.syrjia.common.annotation.Table;

@Table(name = "t_special_test")
public class SpecialTest implements Serializable {

	private static final long serialVersionUID = 1L;

	@Id(increment=false)
	/**
	 * 主键
	 */
	private String id;
	
	/**
	 * 测试名称
	 */
	private String testName;
	
	/**
	 * 摘要
	 */
	private String remark;
	
	/**
	 * 创建用户id
	 */
	private String createUserId;
	
	/**
	 * 另一个名称
	 */
	private String otherName;
	
	/**
	 * 状态
	 */
	private Integer state;
	
	/**
	 * 类型
	 */
	private Integer type;
	
	/**
	 * 是医生
	 */
	private Integer isDoctor;
	
	/**
	 * 是否默认
	 */
	private Integer isDefault;
	
	/**
	 * 舌头
	 */
	private Integer isTongue;
	
	/**
	 * 表层
	 */
	private Integer isSurface;
	
	/**
	 * 另一个
	 */
	private Integer isOther;
	
	/**
	 * 创建时间
	 */
	private Integer createTime;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTestName() {
		return testName;
	}

	public void setTestName(String testName) {
		this.testName = testName;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getCreateUserId() {
		return createUserId;
	}

	public void setCreateUserId(String createUserId) {
		this.createUserId = createUserId;
	}

	public String getOtherName() {
		return otherName;
	}

	public void setOtherName(String otherName) {
		this.otherName = otherName;
	}

	public Integer getState() {
		return state;
	}

	public void setState(Integer state) {
		this.state = state;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Integer getIsDoctor() {
		return isDoctor;
	}

	public void setIsDoctor(Integer isDoctor) {
		this.isDoctor = isDoctor;
	}

	public Integer getIsDefault() {
		return isDefault;
	}

	public void setIsDefault(Integer isDefault) {
		this.isDefault = isDefault;
	}

	public Integer getIsTongue() {
		return isTongue;
	}

	public void setIsTongue(Integer isTongue) {
		this.isTongue = isTongue;
	}

	public Integer getIsSurface() {
		return isSurface;
	}

	public void setIsSurface(Integer isSurface) {
		this.isSurface = isSurface;
	}

	public Integer getIsOther() {
		return isOther;
	}

	public void setIsOther(Integer isOther) {
		this.isOther = isOther;
	}

	public Integer getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Integer createTime) {
		this.createTime = createTime;
	}
}
